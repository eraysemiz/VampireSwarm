using UnityEngine;

public class FireballController : AttackController
{
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    protected override void Start()
    {
        base.Start(); // base parent class taki Start fonksiyonunu çaðýrýr
    }

    protected override void Attack()
    {
        base.Attack();
        GameObject spawnFireball = Instantiate(weaponData.Weapon);
        spawnFireball.transform.position = transform.position;

        Vector3 playerDirection = (pmove.transform.position - transform.position).normalized;

        spawnFireball.GetComponent<FireballBehaviour>().DirectionChecker(playerDirection);
    }
}
